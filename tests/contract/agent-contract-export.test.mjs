test("agent-contract export writes AGENTS.rekon.md from the latest agent-contract Publication", async () => {
  await withFixture(async (root) => {
    runCli(["refresh", "--root", root, "--json"]);
    runCli(["publish", "agent-contract", "--root", root, "--json"]);

    const result = JSON.parse(
      runCli([
        "agent-contract",
        "export",
        "--root",
        root,
        "--output",
        "AGENTS.rekon.md",
        "--json",
      ]).stdout,
    );

    assert.equal(result.wrote, true);
    assert.equal(result.protectedPath, false);
    assert.equal(result.forced, false);
    assert.equal(result.outputPath, "AGENTS.rekon.md");
    assert.equal(result.absolutePath, join(root, "AGENTS.rekon.md"));
    assert.equal(result.publicationRef.type, "Publication");
    assert.ok(typeof result.publicationRef.id === "string" && result.publicationRef.id.length > 0);
    assert.equal(result.publicationRef.schemaVersion, "0.1.0");

    const written = await readFile(join(root, "AGENTS.rekon.md"), "utf8");
    assert.ok(written.length > 0);
  });
});

