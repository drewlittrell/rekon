test("agent-contract export overwrites an existing file when --force is provided", async () => {
  await withFixture(async (root) => {
    runCli(["refresh", "--root", root, "--json"]);
    runCli(["publish", "agent-contract", "--root", root, "--json"]);
    await writeFile(join(root, "AGENTS.rekon.md"), "stale placeholder\n", "utf8");

    const result = JSON.parse(
      runCli([
        "agent-contract",
        "export",
        "--root",
        root,
        "--output",
        "AGENTS.rekon.md",
        "--force",
        "--json",
      ]).stdout,
    );

    assert.equal(result.wrote, true);
    assert.equal(result.forced, true);
    assert.equal(result.protectedPath, false);
    assert.equal(
      result.message,
      "Overwrote existing file because --force was provided.",
    );

    const written = await readFile(join(root, "AGENTS.rekon.md"), "utf8");
    assert.ok(!written.includes("stale placeholder"));
    assert.ok(written.includes("# Rekon Agent Operating Contract"));
  });
});

