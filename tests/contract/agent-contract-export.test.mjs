test("exported file includes the original agent-contract Publication content", async () => {
  await withFixture(async (root) => {
    runCli(["refresh", "--root", root, "--json"]);
    const publishResult = JSON.parse(
      runCli(["publish", "agent-contract", "--root", root, "--json"]).stdout,
    );
    const publicationRef = publishResult.artifacts.find((ref) => ref.type === "Publication");
    const publication = JSON.parse(
      await readFile(join(root, publicationRef.path), "utf8"),
    );

    runCli([
      "agent-contract",
      "export",
      "--root",
      root,
      "--output",
      "AGENTS.rekon.md",
      "--json",
    ]);

    const written = await readFile(join(root, "AGENTS.rekon.md"), "utf8");

    assert.ok(written.includes(publication.content));
    assert.ok(written.includes("# Rekon Agent Operating Contract"));
  });
});

